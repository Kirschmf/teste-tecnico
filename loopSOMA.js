let INDICE = 12;
let SOMA = 0;
let K = 1;

while (K < INDICE) {
    K = K + 1; // Incrementa a letra K
    SOMA = SOMA + K; // Adiciona a letra K à SOMA
}

console.log(SOMA); // Imprime o resultado da SOMA

/* Ao executar o código, a saída será 76, portanto o valor da variável 'SOMA' foi 76. */